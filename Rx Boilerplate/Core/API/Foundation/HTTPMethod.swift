import Foundation

/// HTTP 메소드
enum HTTPMethod: String {
    
    /// GET
    case get = "GET"
    
    /// POST
    case post = "POST"
    
    /// PUT
    case put = "PUT"
    
    /// PATCH
    case patch = "PATCH"
    
    /// DELETE
    case delete = "DELETE"
}
